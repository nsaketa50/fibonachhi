package practise;

public class SwitchCase {
    
    // instead of using my if else statement we can use switch case
    public static void main(String[] args) {

   int age=2;

    switch(age){
        case 1:
        System.out.println("Just a tommy time");
        break;
    
        case 2:
             System.out.println("You can walk");
            break;
    
        default:
            System.out.println("Swing......");;
    }
}
}